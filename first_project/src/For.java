package hu.progmatic;

import java.util.Scanner;

public class For {
    public static void main(String[] args) {

        int szamomFor = 1;
        System.out.println(szamomFor);

        for (szamomFor = 1; szamomFor <= 20; szamomFor++) {
            System.out.print(szamomFor);
            System.out.print(" ");
        }
        System.out.println("END");
        System.out.print(szamomFor);
    }


}
